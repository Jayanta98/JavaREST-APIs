package com.sp.dto;

public class JwtRequest {
	private String myName;
	private String myPass;
	public String getMyName() {
		return myName;
	}
	public void setMyName(String myName) {
		this.myName = myName;
	}
	public String getMyPass() {
		return myPass;
	}
	public void setMyPass(String myPass) {
		this.myPass = myPass;
	}
	
	
}
